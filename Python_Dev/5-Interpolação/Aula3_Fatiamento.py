"""
Fatiamento de strings
Fatiamento [i:f:p] [::]
Obs.: a função len retorna a qtd 
de caracteres da str
"""
variavel = 'Olá mundo'
#Invete a palavra
print(variavel[::-1])
#Mostra apenas o indice
print(variavel[5])
#Remove uma parte
print(variavel[3:])
#Remove o Final
print(variavel[:4])
#Mostra Tamanho
print(len(variavel))