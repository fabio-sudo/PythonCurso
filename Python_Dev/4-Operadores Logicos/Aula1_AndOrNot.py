# Operadores lógicos
# and (e) or (ou) not (não)


# And retorna True se e somente se ambos os operandos forem True. Caso contrário, retorna False. 
print(f"AND: {True and False}")

#OR retorna True se pelo menos um dos operandos for True. Retorna False apenas se ambos os operandos forem False. 
print(f"OR: {True or False}")

#Not inverte o valor do operador
print(f"NOT: {not False}")